    private void btSubmeterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSubmeterActionPerformed
       String nome = tfNome.getText();
       String email =  tfEmail.getText();
        String senha = new String(jPass.getPassword());
        
        
        
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("GestaoUtilizadoresPU");
       UtilizadorJpaController jpa = new UtilizadorJpaController(emf);
       
       
        if (emailJaCadastrado(email, jpa)) {
        JOptionPane.showMessageDialog(rootPane, "Este email já está em uso. Por favor, escolha outro.");
        return; 
    }
        
       if(senha.isEmpty()  || email.isEmpty() || nome.isEmpty()){
           JOptionPane.showMessageDialog(rootPane, "Algum campo em falta!");
           return;
       }
       
       Utilizador ut = new Utilizador();
       
       ut.setNome(nome);
       ut.setEmail(email);
       ut.setPsswd(senha);
       
        try {
            jpa.create(ut);
            JOptionPane.showMessageDialog(rootPane, "Utilizador criado com sucesso");
            segundoMC s = new segundoMC();
            s.setVisible(true);
            this.setVisible(false);
        } catch (Exception ex) {
            Logger.getLogger(principalMC.class.getName()).log(Level.SEVERE, null, ex);
             JOptionPane.showMessageDialog(rootPane, "Não foi possível criar o utilizador");
        }
       
    }//GEN-LAST:event_btSubmeterActionPerformed

