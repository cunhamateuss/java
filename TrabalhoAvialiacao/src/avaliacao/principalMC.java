    private boolean emailJaCadastrado(String email, UtilizadorJpaController jpa) {
    EntityManager em = jpa.getEntityManager();
    TypedQuery<Long> query = em.createQuery("SELECT COUNT(u) FROM Utilizador u WHERE u.email = :email", Long.class);
    query.setParameter("email", email);

    Long count = query.getSingleResult();
    return count > 0; // Retorna verdadeiro se o email já estiver em uso, falso caso contrário
}
    
   
