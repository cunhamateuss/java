    private void fazerReset()
    {
        jButton5.setVisible(true);
    }
    
    private void verifica(int cor)
    {
        if(cor != sequencia.get(jogada)){
            fazerReset();
            //System.out.println("Perdeu");
        }
            else{
            jogada++;
                if (jogada == sequencia.size())
            {
            jogada = 0;
            sequencia.add(gerarNumero());
            varreepisca();
            //System.out.println("fim turno");
            score.setText("Score: "+ (sequencia.size()-1));
            
            }
        }
    }
    
    private void varreepisca()
    {   
        Thread t1 = new Thread() {
            public void run(){
                for(int i=0;i < sequencia.size() ; i++){
                       System.out.println(sequencia.get(0));
                       foco(sequencia.get(i));
                
                try {
                    sleep(1000);
                } catch (InterruptedException ex) {
                    Logger.getLogger(Jogo.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                    try {
                        btnDefault();
                        sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Jogo.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }       
            }
        };
        
        t1.start();
    }
    
