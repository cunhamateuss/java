    private int gerarNumero()
    {
       Random r = new Random();
       return r.nextInt(4);
    }
    
    private void foco(int posicao)
    {
        //System.out.println(posicao);
        
        switch(posicao)
        {
            case 0: btnVerde.requestFocus();
                    btnVerde.setBackground(new java.awt.Color(0, 230, 0));
                    break;
            case 1: btnVermelho.requestFocus();
                    btnVermelho.setBackground(new java.awt.Color(255, 17, 33));
                    break;
            case 2: btnAmarelo.requestFocus();
                    btnAmarelo.setBackground(new java.awt.Color(255, 255, 99));
                    break;
            case 3: btnAzul.requestFocus();
                    btnAzul.setBackground(new java.awt.Color(0, 147, 220));
                    break;       
        }
    }
    
